<p>The Yatzy is played with five dices. The game consists of thirteen rounds, when all rounds have been played the result is presented.</p>
<p>The user rolls the dices a maxium of three rolls per round. The aim is to get as high a score as possible.</p>
<p>To get the bonus of 50 additional points the user must get 63 points or more from the first six rounds (Aces to Sixes).</p>
<p>If the user rolls five dices with the same faces you get Yatzy, also worth 50 points.</p>

